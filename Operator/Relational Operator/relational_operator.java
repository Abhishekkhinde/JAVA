public class relational_operator {
    public static void main(String[] args) {
        int p = 2;
        int q = 2;
        int r = 3;
        int s = 4;

        System.out.println(p==q);
        System.out.println(p!=q);
        System.out.println(p>r);
        System.out.println(p<r);
        System.out.println(p>=s);
        System.out.println(p<=s);
    }
}
